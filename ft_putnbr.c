#include "libft.h"

void	ft_putnbr(int n)
{
	int b;

	if (n == -2147483648)
	{
		write(1, "-2147483648", 11);
		return ;
	}
	if (n < 0)
	{
		ft_putchar('-');
		n *= -1;
	}
	while (n >= 10)
	{
		b = n / 10;
		ft_putnbr(b);
		n = n % 10;
	}
	ft_putchar(n + '0');
}
