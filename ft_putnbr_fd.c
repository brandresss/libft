#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int b;

	if (n == -2147483648)
	{
		write(fd, "-2147483648", 11);
		return ;
	}
	if (n < 0)
	{
		write(fd, "-", 1);
		n *= -1;
	}
	while (n >= 10)
	{
		b = n / 10;
		ft_putnbr_fd(b, fd);
		n = n % 10;
	}
	ft_putchar_fd(n + '0', fd);
}
