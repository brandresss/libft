#include "libft.h"

void	ft_putendl_fd(char const *s, int fd)
{
	int		i;
	char	*s1;

	if (!s)
		return ;
	i = 0;
	s1 = (char*)s;
	while (s[i] != '\0')
	{
		write(fd, &s[i], 1);
		i++;
	}
	write(fd, "\n", 1);
}
