#include "libft.h"

void	ft_putendl(char const *s)
{
	int		i;
	char	*s1;

	if (!s)
		return ;
	i = 0;
	s1 = (char*)s;
	while (s[i] != '\0')
	{
		write(1, &s[i], 1);
		i++;
	}
	write(1, "\n", 1);
}
