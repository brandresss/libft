#include "libft.h"

void	ft_putstr(char const *s)
{
	char	*s1;
	int		i;

	if (s == NULL)
		return ;
	s1 = (char*)s;
	i = 0;
	while (s[i] != '\0')
	{
		write(1, &s[i], 1);
		i++;
	}
}
